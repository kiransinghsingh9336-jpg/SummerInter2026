import os from 'os'
console.log(os.platform());  //win32
console.log(os.arch());  //X64
console.log(os.type());  //Window_NT
console.log(os.release()); // return os verson
console.log(os.cpus());  // return an array of cpu object
console.log(os.totalmem()); // return total system memory in bytes
console.log(os.freemem()); //return total system free memory in bytes
console.log(os.uptime()); // return uptime
console.log(os.hostname()); // hostname
console.log(os.userInfo()); // information of current users




